def assign_animal(wpm):
    """Prints the animal based on the wpm"""

    animals = {
        0: sloth,
        10: snail,
        20: manatee,
        30: human,
        40: gazelle,
        45: ostritch,
        50: cheetah,
        55: swordfish,
        60: goose,
        65: bird,
        70: eagle,
        75: falcon,
        80: dragon
    }
    for key, value in animals.items():
        if round(wpm) >= key and round(wpm) < key + 10:
            value()
            break
        elif round(wpm) >= 80:
            dragon()
            break


